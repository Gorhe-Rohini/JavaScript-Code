//Number properties to the JS Number Object.

//1. MAX_VALUE	: The largest number possible in JavaScript
let x=Number.MAX_VALUE;
console.log(x);

//2. MIN_VALUE	:The smallest number possible in JavaScript
let y = Number.MIN_VALUE;
console.log(y);


//3. MAX_SAFE_INTEGER : The maximum safe integer (253 - 1)
let z = Number.MAX_SAFE_INTEGER;
console.log(z);

//4. MIN_SAFE_INTEGER: The minimum safe integer -(253 - 1)
let a = Number.MIN_SAFE_INTEGER;
console.log(a);


//5. POSITIVE_INFINITY : Infinity (returned on overflow)
let b = Number.POSITIVE_INFINITY;
console.log(b);


//6. NEGATIVE_INFINITY :Negative infinity (returned on overflow)
let c= Number.NEGATIVE_INFINITY;
console.log(c);


//7. NaN : A "Not-a-Number" value
let d = Number.NaN;
console.log(d);
